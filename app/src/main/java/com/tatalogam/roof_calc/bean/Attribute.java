package com.tatalogam.roof_calc.bean;

public class Attribute {
	
	private Model model;
	private String value;
	public Model getModel() {
		return model;
	}
	public void setModel(Model model) {
		this.model = model;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	

}
